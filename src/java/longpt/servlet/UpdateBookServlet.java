package longpt.servlet;

import java.io.IOException;
import java.util.logging.Level;
import java.util.logging.Logger;
import javax.servlet.ServletException;
import javax.servlet.annotation.MultipartConfig;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.Part;
import longpt.product.ProductDAO;
import longpt.product.ProductDTO;

/**
 *
 * @author Long Pham
 */
@MultipartConfig
public class UpdateBookServlet extends HttpServlet {

    private static final String ERROR = "invalid.html";
    private static final String SUCCESS = "SearchBookServlet";
    private final static Logger LOGGER
            = Logger.getLogger(UpdateBookServlet.class.getCanonicalName());

    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String url = ERROR;
        try {
            String productID = request.getParameter("txtProductID");
            String img = request.getParameter("txtBookImg");
            String name = request.getParameter("txtBookName");
            int qtt = Integer.parseInt(request.getParameter("txtBookQuantity"));
            Float price = Float.parseFloat(request.getParameter("txtBookPrice"));
            String des = request.getParameter("txtBookriceDes");
            String category = request.getParameter("cbCategory");
            ProductDAO dao = new ProductDAO();
            boolean check = false;
            ProductDTO product = new ProductDTO(productID, name, des, "", category, img, qtt, price);
            check = dao.updateProduct(product);
            if (check) {
                url = SUCCESS;
            }

        } catch (Exception e) {
            log("Error at UpdateBookServlet: " + e.toString());
        } finally {
            request.getRequestDispatcher(url).forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

    private String getFileName(Part part) {
        final String partHeader = part.getHeader("content-disposition");
        LOGGER.log(Level.INFO, "Part Header = {0}", partHeader);
        for (String content : part.getHeader("content-disposition").split(";")) {
            if (content.trim().startsWith("filename")) {
                return content.substring(
                        content.indexOf('=') + 1).trim().replace("\"", "");
            }
        }
        return null;
    }
}
